package candies.sweets;
public class Baklava{
	private int weight;
	private float cost;
	private String name;
	public Baklava() {
		weight=250;
		cost=100;
		name="Baklava";
	}
	public String getName() {
		return name;
	}
	public float getCost() {
		return cost;
	}
	public int getWeight() {
		return weight;
	}
}