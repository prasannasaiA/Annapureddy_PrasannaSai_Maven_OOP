package candies.sweets;
public class Cannoli{
	private int weight;
	private float cost;
	private String name;
	public Cannoli() {
		weight=500;
		cost=50;
		name="Cannoli";
	}
	public String getName() {
		return name;
	}
	public float getCost() {
		return cost;
	}
	public int getWeight() {
		return weight;
	}
}