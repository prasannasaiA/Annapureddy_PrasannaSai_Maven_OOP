package candies.chocolates;
public class Cadbury{
	private int weight;
	private float cost;
	private String name;
	public Cadbury() {
		weight=500;
		cost=100;
		name="Cadbury";
	}
	public String getName() {
		return name;
	}
	public float getCost() {
		return cost;
	}
	public int getWeight() {
		return weight;
	}
}