package candies.chocolates;
public class Snickers{
	private int weight;
	private float cost;
	private String name;
	public Snickers() {
		weight=250;
		cost=50;
		name="Snickers";
	}
	public String getName() {
		return name;
	}
	public float getCost() {
		return cost;
	}
	public int getWeight() {
		return weight;
	}
}