package candies.chocolates;
public class Kitkat{
	private int weight;
	private float cost;
	private String name;
	public Kitkat() {
		weight=250;
		cost=25;
		name="Kitkat";
	}
	public String getName() {
		return name;
	}
	public float getCost() {
		return cost;
	}
	public int getWeight() {
		return weight;
	}
}